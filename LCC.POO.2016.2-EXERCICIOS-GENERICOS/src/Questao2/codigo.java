package Questao2;

public class codigo {
	
	private String codigo;
	
	public codigo(String codigo){
		this.codigo = codigo;
	}
	
	public String getCodigo(){
		return codigo;
}
	
	public void setCodigo(String codigo){
		this.codigo = codigo;
	}
	
	public String toString(){
		return this.codigo;
	}
}
